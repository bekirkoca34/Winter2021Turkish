package day03_datacasting;

public class C7_PreincrementPostincrement {

	public static void main(String[] args) {
		
		int num = 20 ;
		
		num++;// num=21
		System.out.println(num);// 21
		
		System.out.println(num++); // 21 once yazdırıyor sonra arttiyor
		
		System.out.println(num);// 22
		
		
		System.out.println(++num); // 23 once arttırıyor sonra yazdıryor.
		System.out.println(num);  // 23
		
		
		
		
		
		
		

	}

}
